namespace DEnt
{
    /// <summary>
    /// Useful shortcut to get the mouse button.
    /// </summary>
    /// <remarks>
    /// Useful instead of using the number value, this way you get a text version of it.
    /// </remarks>
    public static class MouseButton
    {
        /// <summary>
        /// The left mouse button.
        /// </summary>
        public const int Left = 0;

        /// <summary>
        /// The right mouse button.
        /// </summary>
        public const int Right = 1;

        /// <summary>
        /// The middle mouse button.
        /// </summary>
        public const int Middle = 2;
    }
}
